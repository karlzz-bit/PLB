import math

# 常量
print("π:", math.pi)
print("e:", math.e)

# 平方根
print("sqrt(16):", math.sqrt(16))

# 幂运算
print("2^3:", math.pow(2, 3))

# 指数函数
print("exp(1):", math.exp(1))

# 对数函数
print("log(10):", math.log(10))
print("log(100, 10):", math.log(100, 10))

# 三角函数
print("sin(π/2):", math.sin(math.pi / 2))
print("cos(π):", math.cos(math.pi))
print("tan(π/4):", math.tan(math.pi / 4))

# 角度和弧度转换
print("degrees(π):", math.degrees(math.pi))
print("radians(180):", math.radians(180))

# 阶乘
print("factorial(5):", math.factorial(5))